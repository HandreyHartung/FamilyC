#include <stdio.h>
#include <stdlib.h> // Incluindo bibliotecas

void main (){ // Função principal //

float a = 10;   // Declarando variaveis //

while( a <= 15){ // Declarando estrutura de repetição WHILE //
    printf("\n%.2f", a); // Exibindo na tela com \n = Nova linha / Proxima linha // %.2f = Contagem de casas dos números //

a++; // Expressão de incremento //

}

return 0; //  é utilizada no final de uma função em C para indicar que a execução da função foi concluída com sucesso //

}
